package Array;

public class OneDArray {
    public static void main(String[] args) {

        //Declaration of array
        int[] rollNums = new int[3];
        // 101,102,103
        rollNums[0] = 101;
        rollNums[1] = 102;
        rollNums[2] = 103;

        //print using loop

        for(int i=0;i<rollNums.length;i++)
            System.out.print(rollNums[i] + " ");


        
    }
}

package Operators;

public class RelationalOperator {
    public static void main(String[] args) {

        //Relational Operator : == , < , > , <= , >= , !=

        int a = 10;
        int b = 10;
        System.out.println(a == b);//true
        System.out.println(a!=b);//false
        System.out.println(a <= b);//true
        System.out.println(a >= b); //true
        System.out.println(a > b);//false
        System.out.println(a < b);//false

    }
}
